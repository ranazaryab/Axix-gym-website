	<b> 9 practical tips for a healthy and sustainable diet</b><br>
	<p>Each of us contributes to the impact that our food system has on the planet. We can all commit to making the world a healthier place to live, through small but achievable changes to our diets.</p>
	<p>
	1. Eat more fruits and vegetables<br>
	2. Eat locally, when in season<br>
	3. Avoid eating more than needed, especially treats<br>
	4. Swap animal protein for plant-based ones<br>
	5. Choose whole grains<br>
	6. Choose sustainably sources seafood<br>
	7. Eat dairy products in moderation<br>
	8. Avoid unnecessary packaging<br>
	9. Drink tap water</p>
	<img src="Sustainable_tips-en.png"><br>
	<p>
		<b>1. Eat more fruits and vegetables:</b><br>
		Fruit and vegetables are good for our health, and most come with a low environmental impact. There are exceptions, as some require a lot of resources to transport and keep fresh, so eating these less frequently can increase the sustainability of our diets.  Examples include:<br>
		<ul>
			<li>fruits and vegetables that are fragile, or require refrigeration (salads and berries)</li><br>
			<li>vegetables that are grown in protected conditions (such as hot-house tomatoes or cucumbers)</li><br>
			<li>foods that use a lot of resources during transport (green beans, mange-touts, or berries imported from the southern hemisphere).</li>
		</ul></p>
	<p>
		<b>2. Eat locally, when in season: </b><br>
		Locally-grown foods can be a sustainable choice, if we choose those that are in season where we live. The cost of producing or storing local foods beyond their natural growing seasons could be higher than shipping foods that are in season somewhere else.</p>
	<p>
		<b>3. Avoid eating more than needed, especially treats: </b><br>
		Consuming only what we need reduces demands on our food supply by decreasing excess production. It also helps to keep us healthy and avoid excessive weight gain. Limiting snacking on energy-dense low-nutrient foods and paying attention to portion sizes are all useful ways to avoid unnecessary overconsumption.</p>
	
	<p>
		<b>4. Swap animal proteins for plant-based ones</b><br>
		In general, more resources are needed to produce animal-based proteins (especially beef), compared to plant-based proteins (such as beans, pulses and some grains). Eating a more plant-based diet also brings health benefits: plant-based food provides more fibre, and has a lower saturated fat content, both of which can contribute to a decreased risk of cardiovascular disease.<br>
	<ul>
		<li>For meat-eaters, limiting meat consumption to 1-2 times a week, having meat-free days and choosing more sustainable meats like chicken over beef can help us reduce our ecological footprint.</li>
		<li>For those choosing a vegan/vegetarian diet, combining different sources of plant-based protein will ensure our protein needs are met.</li></p>
	</ul>
	<p>
		<b>5. Choose whole grains</b><br>
	
		Non-refined cereals are generally less resource intensive to produce than refined ones as they require fewer processing steps. They are also good for health, reducing our risk of cardiovascular diseases, type 2 diabetes, and overweight.<br>
	<ul>
		<li>Whole meal bread, whole grain pasta, unrefined barley, buckwheat and quinoa, are great choices.<br></li>
		<li>Brown rice is a good substitute for white rice, but it should be enjoyed in moderation, as a lot of water is used during its production.</li></p>
	</ul>
	<p>	
		<b>6. Choose sustainably sourced seafood</b><br>
		Fish is a good source of healthy omega-3 fatty acids, which contribute to normal vision, brain function and heart health. However, overfishing is causing wild fish stocks to become depleted. In order to benefit from the necessary nutrients and reduce pressure on wild fish stocks:<br>
	<ul>
		<li>consume fish and seafood 1-2 times weekly to provide the necessary nutrients and reduce pressure on wild fish stocks.</li><br>
		<li>choose fish and seafood marked with a sustainability label from certified organisations such as the Marine Stewardship Council.</li></p>
	</ul>
	<p>
		<b>7. Eat dairy products in moderation</b><br>
	<ul>
		While milk and dairy production has an important environmental impact, dairy products are an important source of protein, calcium and essential amino acids, and have been linked to reduced risk of several chronic diseases, including metabolic syndrome, high blood pressure, stroke, bowel cancer and type 2 diabetes.<br>
		<li>Enjoy low-fat unsweetened dairy products daily, but in moderation.</li>
		<li>Limit consumption of high-fat cheeses to occasional.</li>
		<li>For those of us who choose to eliminate dairy completely, opt for plant-based drinks that are fortified with vitamins and minerals, like calcium.</li></p>
	</ul>
	<p>
		<b>8. Avoid unnecessary packaging</b><br>
		Food packaging, especially when made of non-recyclable materials can have a huge impact on the environment. We all can reduce the amount of packaged products we buy (think of bulk apples versus cling-film wrapped ones), or opt for materials that are biodegradable, fully recyclable, or made from recycled materials.</p>
	<p>
		<b>9. Drink tap water</b><br>
		In Europe, the standards of water quality and safety are high. Instead of buying bottled water, we can re-fill a reusable water bottle at the tap as many times as we want. Tap water costs a fraction of the price of bottled water and reduces our ecological footprint.</p>
	
	
	
	
